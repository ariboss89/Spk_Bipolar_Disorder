    private void IdPengetahuan() {
        java.sql.Connection conn= new koneksi().connect();        
        try{
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery("select * from pengetahuan order by Id DESC");
            if(rs.first() == false){
                pengetahuan.setIdPengetahuan("K001");
            }
            else{
                rs.first();
                System.out.println("COT: "+ rs.getString("Id").substring(3, 4));
                int nokirim = Integer.valueOf(rs.getString("Id").substring(2, 4))+1;
                System.out.println(nokirim);
                if(nokirim<10){
                    pengetahuan.setIdPengetahuan("K00"+nokirim);
                    pengetahuan.setId(nokirim);
                }else if(nokirim>=10 && nokirim<100){
                    pengetahuan.setIdPengetahuan("K0"+nokirim);
                    pengetahuan.setId(nokirim);
                }else if(nokirim>=100 && nokirim<1000){
                    pengetahuan.setIdPengetahuan("K"+nokirim);
                    pengetahuan.setId(nokirim);
                }
            }
            rs.close();
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, "Data tidak ditemukan");
        }
    }
    
