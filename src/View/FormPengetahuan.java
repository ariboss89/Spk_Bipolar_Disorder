    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        int myid = pengetahuan.getId();
        String Idpengetahuan = pengetahuan.getIdPengetahuan();
        String Idpenyakit = jComboBox1.getSelectedItem().toString();
        String penyakit = txtNamaPenyakit.getText().trim();
        String info = txtInfo.getText().trim();
        String solusi = txtSolusi.getText().trim();
        String Idgejala = jComboBox2.getSelectedItem().toString();
        String gejala = txtNamaGejala.getText().trim();
        Double nilaicf = Double.parseDouble(txtNilaiGejala.getText().trim());

        if (penyakit.equals("")) {
            JOptionPane.showMessageDialog(null, "Field Nama Tidak Boleh Kosong");
            txtNamaPenyakit.requestFocus();
        }else if (info.equals("")) {
            JOptionPane.showMessageDialog(null, "Field Informasi Tidak Boleh Kosong");
            txtInfo.requestFocus();
        }else if (solusi.equals("")) {
            JOptionPane.showMessageDialog(null, "Field Solusi Tidak Boleh Kosong");
            txtSolusi.requestFocus();
        }else if (gejala.equals("")) {
            JOptionPane.showMessageDialog(null, "Field Nama Gejala Tidak Boleh Kosong");
            txtNamaGejala.requestFocus();
        }else if (txtNilaiGejala.equals(0)) {
            JOptionPane.showMessageDialog(null, "Field Nilai CF Tidak Boleh Nol");
            txtSolusi.requestFocus();        
        }else {
            pengetahuan.Save(myid, Idpengetahuan, Idpenyakit, penyakit, info, solusi, Idgejala, gejala, nilaicf);
            Refresh();
        }
    }//GEN-LAST:event_btnSaveActionPerformed

