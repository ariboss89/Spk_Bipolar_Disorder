    private void jComboBox1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox1ItemStateChanged
        // TODO add your handling code here:
        java.sql.Connection conn = new koneksi().connect();
        try{
            java.sql.Statement stmt = conn.createStatement();
            java.sql.ResultSet res = stmt.executeQuery("select *from penyakit where Id = '"+jComboBox1.getSelectedItem()+"'");
            while(res.next()){
                txtNamaPenyakit.setText(res.getString("nama"));
                txtInfo.setText(res.getString("info"));
                txtSolusi.setText(res.getString("solusi"));
            }
        }catch(SQLException ex){
            
        }
    }//GEN-LAST:event_jComboBox1ItemStateChanged

